export function getVenomPrice(code: string) {
  return VENOM_PRICE_BY_CODE[code];
}

export function formatPenPrice(price: number) {
  return `S/ ${price.toFixed(2)}`;
}

export function validateVenomPricing(products: VenomProduct[]) {
  const pricedProducts = products.filter((product) => getVenomPrice(product.code) !== undefined);

  return {
    pricedProducts,
    pricedCount: pricedProducts.length,
    missingCount: products.length - pricedProducts.length,
  };
}